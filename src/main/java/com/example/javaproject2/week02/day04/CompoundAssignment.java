package com.example.javaproject2.week02.day04;

public class CompoundAssignment {
    public static void main(String[] args) {
        int now = 0;
        
        now += 100;
        System.out.println("now = " + now);
        
        now -= 50;
        System.out.println("now = " + now);
        
        now *= 10;
        System.out.println("now = " + now);
        
        now /= 5;
        System.out.println("now = " + now);
        
    }
}
