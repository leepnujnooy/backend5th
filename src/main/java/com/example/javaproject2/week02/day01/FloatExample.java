package com.example.javaproject2.week02.day01;

public class FloatExample {
    public static void main(String[] args) {
        float f01 = 3.141592556f;
        System.out.println("f01 = " + f01);
        double d01 = 3.1490235489346893;
        System.out.println("d01 = " + d01);

        float f02 = 1.23E5f;
        float f03 = 1.23E6f;
        float f04 = 1.23E7f;
        double d04 = 1.23E7;
        System.out.println(f02);
        System.out.println(f03);
        System.out.println(f04);
        System.out.println(d04);
    }
}
