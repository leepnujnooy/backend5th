package com.example.javaproject2.codeUp;

import java.util.Scanner;

public class codeUp1072 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int count = sc.nextInt();
        while(count > 0){
            int num = sc.nextInt();
            System.out.println(num);
            count--;
        }

    }
}
