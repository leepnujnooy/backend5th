package com.example.javaproject2.week03.day01;

public class ForEx {
    public static void main(String[] args) {
        for(char c = 'a'; c<='z';c++){
            System.out.printf("%d:%c ",(int)c,c);
        }
        for(char c = '가';c<='까';c++){
            System.out.println(c);
        }
    }
}
