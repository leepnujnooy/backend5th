package com.example.javaproject2.week03.day02;

public class DoubleForEx {
    public static void main(String[] args) {

        //i,j를 잘 배치
        for(int i=0;i<=5;i++){//첫번째 주사위
            for(int j=0;j<=4;j++){//두번째 주사위
                System.out.printf("%d %d \n",i,j);
            }
        }
    }
}
