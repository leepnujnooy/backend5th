package com.example.javaproject2.week04.day04;

public class Address {

    private String siGu;
    private String doRo;

    public Address(String siGu, String doRo) {
        this.siGu = siGu;
        this.doRo = doRo;
    }

    public String getSiGu() {
        return siGu;
    }

    public String getDoRo() {
        return doRo;
    }
}
