package com.example.javaproject2.week02.day05;

public class ElseEx {
    public static void main(String[] args) {
        boolean checkTrue = true;
        if(checkTrue){
            System.out.println("T");
        }
        else{
            System.out.println("F");
        }

        boolean checkFalse = false;
        if(checkFalse){
            System.out.println("F");
        }
        else{
            System.out.println("T");
        }


    }
}
